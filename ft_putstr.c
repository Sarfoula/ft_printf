#include "ft_printf.h"

ssize_t	ft_putstr(char *s)
{
	ssize_t	i;

	i = 0;
	if (!s)
		return (6);
	while (s[i])
	{
		if (!ft_putchar(s[i]))
			return (-1);
		i++;
	}
	return (i);
}
